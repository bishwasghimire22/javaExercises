// Create a program called NumbersProgram that prints integers between -20 and -1 as shown in the example output. 
// Please use a for loop in your program. 
package first_java_programs;

public class NumbersProgram {

	public static void main(String[] args) {
		for (int i = -20; i <= -1; i++) {
			System.out.print(i + " ");

		}

	}

}
